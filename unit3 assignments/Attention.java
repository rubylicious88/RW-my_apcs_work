public class Attention {

    public static boolean attention(String input) {
        return input.startsWith("Hey, you!");
    }

    public static void main(String[] args) {
        // Test cases
        System.out.println(attention("Hello, my name is Inigo Montoya.")? "true" : "false"); //false
        System.out.println(attention("Excuse me, Dr. Kessner?")? "true" : "false"); //  false
        System.out.println(attention("Hey, you! Give me your code!")? "true" : "false"); //  true
    }
}
